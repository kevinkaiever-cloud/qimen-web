/**
 * 奇门断事逻辑 - 从雨霖课程字幕中整理
 * 供 API 解读时强制遵循，提高断事准确性
 */

const DUANSHI_LOGIC = `
## 断事逻辑（课程提炼，解读时必须遵循）

### 一、总则：一盘一事
- 一次只问一件事，问什么取什么用神；没有针对性则意义不大。
- 奇门是「看图运算」：找到代表「人」与「事」的符号，用五行生克等法则运算得出答案。
- 坐标取「上边」天盘干，不取下边地盘干（除特别约定）。

### 二、坐标定位（用神取法）
- **年干**：长辈、领导、上级、太岁。
- **月干**：平辈、同事、同学、兄弟姐妹、朋友。
- **日干**：求测人自己、问事主体。
- **时干**：所问之事、事件过程与结果、晚辈、学生。
- **婚姻感情**：庚=男方，乙=女方，六合=感情走向、合作；看双方落宫生克与门（心态）。
- **工作/升职**：日干=我，时干或开门=工作/升职这件事；杜门=停滞、堵塞。
- **财运**：生门、戊己、天乙（生我之干）；开门=能借到钱、有机会。
- **疾病**：天芮、死门、伤门、时干；九星=抗病能力，日干旺衰=自身能力。

### 三、解盘步骤（按顺序执行）
1. **定人**：找到代表求测人（或关系人）的用神落宫（看天盘干）。
2. **定事**：找到代表「所问之事」的用神落宫（如升职看时干、工作看开门等）。
3. **看生克**：两宫五行关系——相生=有利/助力/加分，相克=不利/消耗/压制；谁克谁即谁主导。
4. **看门**：该宫八门=心态、行为、态度。吉门（开休生）多主顺；死门=绝望/停滞，杜门=保守/堵塞，遇人事多「停顿卡字」。
5. **看星**：九星=在该宫位的环境、运气、条件（如能否找到工作、配偶，抗病能力）。
6. **看神**：八神定该宫整体性质（吉凶倾向、玄机）。
7. **看旺衰**：日干旺=有能力，九星旺=有运气；门受制/门破=在该地发挥不出能力。

### 四、单宫分层解读（符号分层法）
- **神** → 定该宫**性质**（吉凶倾向）。
- **门 + 干** → **现状**、心态与行为。
- **星 + 干** → **背景**、背后成因、环境与运气。
- **干** → **趋势**、发展方向。
解读时：先看神定性质，再观门星与干的关系，最后综合现状、背景、趋势下结论。

### 五、九宫八卦象意（落宫大方向）
- **坎**：水、动荡、向下、变化、漩涡；不旺=挣扎、困局、越跳越低。
- **艮**：山、阻碍、瓶颈、积累、稳固；也可主积累成事、长期问题。
- **震**：雷、动、阶段性、短期、劳累；公司=前期好后期差、持续性差。
- **巽**：风、选择多、决策难、传播。
- **离**：火、上升、进取、快速发展。
- **坤**：地、配合、行政、包容、秘书助理类。
- **兑**：泽、陷溺、杂乱、挣扎、无方向。
- **乾**：天、重要、核心、大单位、顶天立地；开门落乾=晋升空间满、拥挤。

### 六、五行生克在断事中的用法
- 相生=加号，增加能量、有利；相克=减号，压制、不利。
- 女方宫克男方宫=女方主导、女方不满；男方宫生女方宫=男方付出、女方得助。
- 用神宫与事宫相生主顺势；用神宫被事宫克=此事消耗我、难成或费力。

### 七、空亡
- 用神落宫空亡=心虚、缺失、名存实亡、权利或条件不成熟。
- 空亡看对宫：对宫多为事情下一步或填实后的走向。
- 工作空亡填实=找到工作、事成。

### 八、应期判断前提
- 先断此事会不会成、会不会发生，再断应期；事不成则不断应期。
- 常用：冲以合为应期、合以冲为应期；人/事发生变化之时为应期；生逢生日、克逢克日等。

### 九、断事输出要求
1. 严格针对用户问题，找准「人」与「事」两处用神，再论生克与门星神。
2. 先给结论倾向（成/不成、顺/逆、利/不利），再分层简述理由（宫位、门、星、生克）。
3. 最后用 1～2 句通俗总结，便于用户一眼看懂。
`;

/**
 * 返回断事逻辑文本，供拼进 system prompt
 */
function getDuanshiLogicPrompt() {
  return DUANSHI_LOGIC;
}

